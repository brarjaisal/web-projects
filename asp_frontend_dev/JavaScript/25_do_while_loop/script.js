let i = 0 
do {
    console.log(i);
    i++
} while (i < 5)

console.log('Program Ended')

// Output:
// Program Start
// 0
// 1
// 2
// 3
// 4
// Program Ended

// Explanation:
// The code demonstrates the use of a do...while loop in JavaScript. 
// It initializes a variable 'i' to 0 and then enters the do block,
// where it prints the value of 'i' and increments it by 1. 
// The loop continues to execute as long as 'i' is less than 5. 
// This ensures that the block of code runs at least once before checking the condition.    

